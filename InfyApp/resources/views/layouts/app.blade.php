        $(document).ready(function() {
        $('#example').DataTable( {
            "ordering": false,
            initComplete: function () {
                this.api().columns([2,3,4]).every( function (d) {//THis is used for specific column
                var column = this;
                var theadname = $('#example th').eq([d]).text();
                var select = $('<select class="mx-1 form-control"><option value="">'+theadname+': All</option></select>')
                .appendTo( '#filter_table' )
                .on( 'change', function () {
                    var val = $.fn.dataTable.util.escapeRegex(
                    $(this).val()
                    );
    
                    column
                    .search( val ? '^'+val+'$' : '', true, false )
                    .draw();
                } );
                column.data().unique().sort().each( function ( d, j ) {
                    var val = $('<div/>').html(d).text();
                    select.append( '<option value="'+val+'">'+val+'</option>' )
                } );
                } );
            }
            } );
        } );
    </script>

